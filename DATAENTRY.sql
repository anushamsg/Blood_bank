CREATE TABLE DONORS (
    DON_ID INT,
    DON_NAME VARCHAR(75),
    DON_AGE NUMERIC(3,0),
    DON_GENDER BIT,
    DON_BLOODTYPE CHAR(3) CHECK (DON_BLOODTYPE IN ('A+', 'A-', 'B+', 'B-', 'AB+', 'AB-', 'O+', 'O-')),
    PRIMARY KEY (DON_ID)
);
 
 
CREATE TABLE STAFF (
    STAFF_ID INT,
    STAFF_NAME VARCHAR(75),
    STAFF_ROLE VARCHAR(25),
    STAFF_PHONE_NUMBER VARCHAR(15),
    PRIMARY KEY (STAFF_ID)
);
 
CREATE TABLE REQUEST (
    REQUE_ID INT,
    REQUE_BLOODTYPE CHAR(3) CHECK (REQUE_BLOODTYPE IN ('A+', 'A-', 'B+', 'B-', 'AB+', 'AB-', 'O+', 'O-')),
    REQUE_BLOODQUAN NUMERIC(3,0),
    REQUE_HOSP VARCHAR(75),
    PRIMARY KEY (REQUE_ID)
);
 
CREATE TABLE DONATION (
    DONA_ID INT,
    DON_ID INT,
    STAFF_ID INT,
    DONA_DATE DATE,
    PRIMARY KEY (DONA_ID),
    FOREIGN KEY (DON_ID) REFERENCES DONORS(DON_ID),
    FOREIGN KEY (STAFF_ID) REFERENCES STAFF(STAFF_ID)
);
 
CREATE TABLE BLOOD (
    BLOOD_ID INT,
    DONA_ID INT,
    BLOOD_EXP_DATE DATE,
    BLOOD_PRO_TYPE VARCHAR(20),
    PRIMARY KEY (BLOOD_ID),
    FOREIGN KEY (DONA_ID) REFERENCES DONATION(DONA_ID)
);

CREATE TABLE TRANSFERS (
    TRANS_ID INT,
    BLOOD_ID INT,
    REQUE_ID INT,
    STAFF_ID INT,
    TRANS_DATE DATE,
    TRANS_COMPA_RESULT BIT,
    PRIMARY KEY (TRANS_ID),
    FOREIGN KEY (STAFF_ID) REFERENCES STAFF(STAFF_ID),
    FOREIGN KEY (BLOOD_ID) REFERENCES BLOOD(BLOOD_ID),
    FOREIGN KEY (REQUE_ID) REFERENCES REQUEST(REQUE_ID)
);

